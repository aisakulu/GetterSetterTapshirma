package com.peakSoft;

public class Kurs {

    private String nameKurs;
    private int numberKurs;
    private String infoTeacher;
    private int dayKurs;

    public String getNameKurs() {
        return nameKurs;
    }

    public void setNameKurs(String nameKurs) {
        this.nameKurs = nameKurs;
    }

    public int getNumberKurs() {
        return numberKurs;
    }

    public void setNumberKurs(int numberKurs) {
        this.numberKurs = numberKurs;
    }

    public String getInfoTeacher() {
        return infoTeacher;
    }

    public void setInfoTeacher(String infoTeacher) {
        this.infoTeacher = infoTeacher;
    }

    public int getDayKurs() {
        return dayKurs;
    }

    public void setDayKurs(int dayKurs) {
        this.dayKurs = dayKurs;
    }
}
